use colored::*;

/// 格式化说明符：控制输出的宽度、对齐、精度、进制等
/// 1、宽度与对齐：{:>10} 右对齐、{:<10} 左对齐、{:^10} 居中
/// 2、精度：{:.2} 保留小数位数
/// 3、进制：{:b} 二进制、{:o} 八进制、{:x} 十六进制
/// 4、科学计数法：{:e}
/// 5、补零：{:05}
fn main() {
    // 1、宽度与对齐
    println!("{}", "=== 宽度与对齐 ===".green());
    println!("[{:>10}]", "right"); // 右对齐，总宽度 10
    println!("[{:<10}]", "left"); // 左对齐
    println!("[{:^10}]", "center"); // 居中
                                    // 用指定字符填充
    println!("[{:*>10}]", "right"); // 左侧用 * 填充
    println!("[{:*<10}]", "left"); // 右侧用 * 填充
    println!("[{:*^10}]", "center"); // 两侧用 * 填充
    println!();

    // 2、数字精度
    println!("{}", "=== 数字精度 ===".green());
    let pi = 3.14159265;
    println!("默认：{}", pi);
    println!("两位：{:.2}", pi); // 保留 2 位小数
    println!("四位：{:.4}", pi); // 保留 4 位小数
    println!("宽度+精度：{:>8.2}", pi); // 总宽度 8，保留 2 位
    println!();

    // 3、进制转换
    println!("{}", "=== 进制转换 ===".on_green());
    let num = 42;
    println!("十进制：{}", num);
    println!("二进制：{:b}", num); // 101010
    println!("八进制：{:o}", num); // 52
    println!("十六进制：{:x}", num); // 2a
    println!("十六进制(大写)：{:X}", num); // 2A
                                           // 带前缀的进制输出
    println!("带前缀：{:#b} {:#o} {:#x}", num, num, num);
    println!();

    // 4、科学计数法
    println!("{}", "=== 科学计数法 ===".green());
    let big = 1234567.89;
    println!("默认：{}", big);
    println!("科学计数法：{:e}", big); // 小写 e
    println!("科学计数法：{:E}", big); // 大写 E
    println!();

    // 5、补零
    println!("{}", "=== 补零 ===".green());
    println!("[{:05}]", 42); // 00042
    println!("[{:05}]", -42); // -0042
    println!("[{:08.2}]", 3.14); // 00003.14
}
