/// # 06 注释（Comments）
///
/// Rust 支持三种注释：行注释、块注释、文档注释。
/// 文档注释可以被 `cargo doc` 工具提取生成 HTML 文档。
///
/// 运行：`cargo run --example 06_comments`

use colored::*;

fn main() {
    // ========================================
    // 1. 行注释（Line Comments）
    // ========================================
    println!("{}", "\n=== 行注释 ===".green().bold());

    // 这是单行注释，编译器会完全忽略这一行
    let x = 42; // 行尾也可以加注释
    println!("x = {x}");

    // ========================================
    // 2. 块注释（Block Comments）
    // ========================================
    println!("{}", "\n=== 块注释 ===".green().bold());

    /* 这是块注释
       可以跨越多行
       适合暂时屏蔽一整段代码 */

    let a = 1 /* 注释也可以插在表达式中间 */ + 2;
    println!("a = {a}");

    // 嵌套块注释是 Rust 的特色！
    /* 外层注释开始
       /* 内层注释也 OK */
       外层注释结束 */
    println!("嵌套块注释没问题");

    // ========================================
    // 3. 文档注释（Doc Comments）
    // ========================================
    println!("{}", "\n=== 文档注释 ===".green().bold());

    // /// — 外部文档注释（修饰紧随其后的项）
    // //! — 内部文档注释（修饰所在的项本身，常用于模块根部）

    // 调用带文档注释的函数，文档注释不影响运行
    let result = add(3, 4);
    println!("add(3, 4) = {result}");

    println!(
        "\n{}",
        "提示：运行 `cargo doc --open` 可生成并打开项目文档".yellow()
    );
}

/// 将两个整数相加并返回结果。
///
/// # Examples
///
/// ```
/// let sum = 6 + 7;
/// assert_eq!(sum, 13);
/// ```
fn add(a: i32, b: i32) -> i32 {
    a + b
}
